#include<stdio.h>
double fun(int n)
{
/***********SPACE***********/
double s,t;
int i;
 /***********SPACE***********/
s=1; 
t=1.0;
for(i=1;i<=n;i++)
{
 /***********SPACE***********/
t=t*i/(i*2+1);
s=s+t; 
}
/***********SPACE***********/
return s;
}
