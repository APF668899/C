#include<stdio.h>
#include<math.h>
/***********SPACE***********/
long f(long n)
{
	long m,y = 0;
	/***********SPACE***********/
	m=fabs(n);
	while(m!=0){
		y=y*10+m%10;
		/***********SPACE***********/
		m/=10;
	}
	if(n>0)
	   return y;
	else
	/***********SPACE***********/
	   return -y;
}
void main()
{
	printf("%ld\t",f(12345));
	printf("%ld\n",f(-34567));
}

