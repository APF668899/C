#include  <stdlib.h>
#include  <conio.h>
#include  <stdio.h>
#include  <math.h>
double fun(int k)
{ 
        int n; float s, w, p, q;
        n=1;
        s=1.0;
/***********SPACE***********/
        while(n<=k)
        {         	
            w=2.0*n;//2,4,6
            p=w-1.0;//1,3,5
/***********SPACE***********/
            q=w+1.0;//3,5,7
/***********SPACE***********/
			printf("n=%d w=%lf p=%lf q=%lf s=%lf\n",n,w,p,q,s);
            s=s*w*w/p/q;
            n++;
        }//1.533852。S＝(2*2/(1×3))×(4*4/(3×5))×(6*6/(5×7))×…

/***********SPACE***********/
       return s;
}
