#include <stdio.h>
/***********SPACE***********/
double fun(int n)   
{
/***********SPACE***********/
  double y=0;
  int i;
  for(i=1; i<=n; i++)
  {
/***********SPACE***********/
     y=y+1.0/(2*i*i); 
  }
/***********SPACE***********/
  return(y);
}
main()   
{
  int n;   
  printf("Enter n: ");   
  scanf("%d", &n); 
/***********SPACE***********/ 
  printf("\nThe result is %lf\n", fun(n)); 
}


