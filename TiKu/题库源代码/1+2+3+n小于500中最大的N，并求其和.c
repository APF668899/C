/*功能：求满足1+2+3+...n<500中最大的N，并求其和。

-------------------------------------------------------*/
#include <stdio.h>
void main()
{
  /***********SPACE***********/
  int n=0;
  int sum = 0;
  /***********SPACE***********/
  while(sum < 500)
  {
      /***********SPACE***********/
     n++;
      /***********SPACE***********/
     sum += n;
  }
  /***********SPACE***********/
  printf("n=:%d\n",n);
  printf("1+2+3+...+%d<500\n",sum);
}

